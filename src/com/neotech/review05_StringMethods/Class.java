package com.neotech.review05_StringMethods;

public class Class {
	
	// No Parameters and NO Return type
	
	void addTwoNumbers() {
	int result = 8 + 7;
	System.out.println(result);
	}
	
	// Parameters and NO Return type
	
	void addMineTwoNumbers(int i, int j) {
		
		int result = i + j;
		System.out.println(result);
	}

	// No Parameters and Return type
	
	int addTwoNumbersWithResult () {
		int result = 8 +7;
		return result;
	
	}
	
	// Parameters and Return type
	
	int addMineTwoNumbersWithResult (int x, int y) {
		int result = x + y;
		return result;
		
	}
}
